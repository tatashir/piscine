#include <unistd.h>

int	main(int argc, char **argv)
{
	int	c;
	int	d;

	c = argc - 1;
	d = 0;
	if (1 < argc)
	{
		while (0 < c)
		{
			d = 0;
			while (argv[c][d] != '\0')
			{
				write(1, &argv[c][d], 1);
				d++;
			}
			write(1, "\n", 1);
			c--;
		}
	}
	return (0);
}
