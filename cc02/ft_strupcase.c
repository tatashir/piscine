char	*ft_strupcase(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if (('a' <= str[c]) && (str[c] <= 'z'))
			str[c] -= 32;
		c++;
	}
	return (str);
}
