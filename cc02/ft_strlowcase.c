char	*ft_strlowcase(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if (('A' <= str[c]) && (str[c] <= 'Z'))
			str[c] += 32;
		c++;
	}
	return (str);
}
