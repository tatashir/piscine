int	ft_strcmp(char *str1, char *str2)
{
	int	c;

	c = 0;
	while ((str1[c] == str2[c]) && (str1[c] != '\0') && (str2[c] != '\0'))
	{
		c++;
	}
	return (str1[c] - str2[c]);
}
